public class Hero {

    private int heroHitPoints;
    private int heroStrength;
    private int heroDexterity;
    private int heroMagicka;
    private int heroMana;

    public int getHeroStrength() {
        return heroStrength;
    }

    public void setHeroStrength(int heroStrength) {
        this.heroStrength = heroStrength;
    }

    public int getHeroDexterity() {
        return heroDexterity;
    }

    public void setHeroDexterity(int heroDexterity) {
        this.heroDexterity = heroDexterity;
    }

    public int getHeroMagicka() {
        return heroMagicka;
    }

    public void setHeroMagicka(int heroMagicka) {
        this.heroMagicka = heroMagicka;
    }

    public int getHeroHitPoints() {
        return heroHitPoints;
    }

    public void setHeroHitPoints(int heroHitPoints) {
        this.heroHitPoints = heroHitPoints;
    }

    public int getHeroMana() {
        return heroMana;
    }

    public void setHeroMana(int heroMana) {
        this.heroMana = heroMana;
    }
}
